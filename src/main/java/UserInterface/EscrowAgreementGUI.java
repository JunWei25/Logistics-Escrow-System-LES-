    public void loadEscrowAgreementTextFields(){
        String databaseFolder = "database/";
        String escrowAgreementFile = databaseFolder+"EscrowAgreement.txt";
        try {
            File myObj = new File(escrowAgreementFile);
            Scanner readFile = new Scanner(myObj);
            while (readFile.hasNextLine()) {
                String line = readFile.nextLine().toString();
                if(line.contains(packageNo)){
                    result.add(line);
                };
            }
            readFile.close();
            for(int i=0; i<result.size();i++){
                String getColumn = result.get(i);
                String[] column = getColumn.split("!");
                
                packageNoTextField.setText(column[0]);
                senderNameTextField.setText(column[1]);
                senderStatusTextField.setText(column[2]);
                recipientNameTextField.setText(column[3]);
                recipientStatusTextField.setText(column[4]);
                witnessByTextField.setText(column[5]);
                witnessStatusTextField.setText(column[6]);
                escrowFeeTextField.setText(column[8]);
                dateTextField.setText(column[9]);
            }
            result.clear();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
    }

