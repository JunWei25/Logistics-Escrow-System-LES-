    private void agreeButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_agreeButtonMouseClicked
        // TODO add your handling code here:
        String databaseFolder = "database/";
        String escrowAgreementFile = databaseFolder+"EscrowAgreement.txt";
        String parcelNo="";
        String line = "";
        String replaceLine = "";
        String witness = "";
        Boolean isSender =false;
        Boolean isRecipient = false;
        String fee="";
        int columnNo=0;
        int confirm = JOptionPane.showConfirmDialog(null, String.format("Are you sure you want to accept escrow agreement?" ), "Confirm Escrow Agreement", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
        try {
                
                File myObj = new File("database/EscrowAgreement.txt");
                Scanner readFile = new Scanner(myObj);
                while (readFile.hasNextLine()) {
                    line = readFile.nextLine().toString();
                  if(line.contains(packageNo)){
                        result.add(line);
                        replaceLine = line;
                  };
                }
                          
                readFile.close();
                    } catch (FileNotFoundException e) {
                e.printStackTrace();
            }        
        if(currentUser.getFirstName().equals("Admin")){
            columnNo=6;
        }
        else{
            
            String userName = currentUser.getFirstName()+" "+currentUser.getLastName();
            
                for(int i=0; i<result.size();i++){
                    String getColumn = result.get(i);
                    String[] column = getColumn.split("!");
                
                    parcelNo = column[0];
                    String sender = column[1];
                    String receiver = column[3];
                    witness = column[5];
                    String status = column[7];
                    fee = column[8];
                    if(userName.equals(sender)){
                        isSender = true;
                        columnNo = 2;
                    }else{
                        isRecipient = true;
                        columnNo = 4;
                    }
            }
                
        }
        if(result.size()!=0){
            String[] column;
            for(int i=0; i<result.size(); i++){
                column = result.get(i).split("!");
                column[columnNo] = "Agreed";
                String concatenatedString = "";
                String delimiter = "!";
                for (String word : column) {
                    concatenatedString += concatenatedString.equals("") ? word : delimiter + word;
                }
                
                try {
                    File escrowFile = new File("database/EscrowAgreement.txt");
                    Scanner readEscrowFile = new Scanner(escrowFile);
                    StringBuffer buffer = new StringBuffer();
                    while (readEscrowFile.hasNextLine()) {
                        buffer.append(readEscrowFile.nextLine()+System.lineSeparator());
                }   
                        String fileContents = buffer.toString();
                        
                        String updateLine = fileContents.replaceAll(replaceLine, concatenatedString);
                        FileWriter writer;
                        
                    FileWriter myWriter = new FileWriter("database/EscrowAgreement.txt");
                    myWriter.write(updateLine);
                    myWriter.close();
                    System.out.println("Successfully updated escrow agreement status");
                    
                    updateEscrowAgreementStatus(packageNo);
                        
                } catch (IOException ex) {
                    Logger.getLogger(EscrowAgreementGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        result.clear();
        JOptionPane.showMessageDialog(null, "Successfully accepted escrow agreement!");
        loadEscrowAgreementTextFields();
        String userName = currentUser.getFirstName()+" "+currentUser.getLastName();
        if(isRecipient==true){
            new MakePaymentGUI(currentUser,fee,witness, packageNo,"Escrow Agreement Fee").setVisible(true);
        }
        }
    }//GEN-LAST:event_agreeButtonMouseClicked

