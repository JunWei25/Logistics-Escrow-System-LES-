    private void updateDeliveryStatusButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateDeliveryStatusButtonMouseClicked
        // TODO add your handling code here:
        String selectedDeliveryStatus = String.valueOf(deliveryStatusComboBox.getSelectedItem());
        int confirm = JOptionPane.showConfirmDialog(null, String.format("Are you sure you want to edit parcel delivery status to '"+selectedDeliveryStatus +" '?" ), "Confirm Edit", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel)packagesTable.getModel();
            int selectedItemRowIndex = packagesTable.getSelectedRow();
        
            String packageNo = model.getValueAt(selectedItemRowIndex, 0).toString();
            String senderName = model.getValueAt(selectedItemRowIndex, 1).toString();
            String receipientName = model.getValueAt(selectedItemRowIndex, 2).toString();
        
            Parcel parcel = new Parcel(packageNo, senderName, receipientName);
            try {
                if(Utils.updateDeliveryStatus(parcel, selectedDeliveryStatus )==true){
                    JOptionPane.showMessageDialog(null, "Parcel delivery status has been successfully updated to '"+selectedDeliveryStatus +" '!");
                    if(selectedDeliveryStatus.equals("Delivered")){
                        String fee = model.getValueAt(selectedItemRowIndex, 4).toString();
                        new MakePaymentGUI(currentUser,fee,senderName,packageNo,"Item Delivered Releasing Money").setVisible(true);
                        
                    }
                    else if(selectedDeliveryStatus.equals("Completed")){
                        Utils.addNewBlock(packageNo);
                    }
                }
            } catch (IOException ex) {
                Logger.getLogger(ManagePackageGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            refresh();
        }
    }//GEN-LAST:event_updateDeliveryStatusButtonMouseClicked

