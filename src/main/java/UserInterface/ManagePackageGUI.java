    private void loadTable() {
        SymmetricEncryption symm = new SymmetricEncryption();
        DefaultTableModel tableModel = (DefaultTableModel)packagesTable.getModel();
        try {
            File myObj = new File("database/Parcel.txt");
            Scanner readFile = new Scanner(myObj);
            while (readFile.hasNextLine()) {
                String line = readFile.nextLine().toString();
//                if(line.contains("Processing")){
                    result.add(line);
//                };
            }
            readFile.close();
            System.out.println(result);
            for(int i=0; i<result.size();i++){
                String getColumn = result.get(i);
                String[] column = getColumn.split("!");
                
                String secretKey = column[15];
                byte[] decodeKey = Base64.getDecoder().decode(secretKey);
                Key decryptKey = new SecretKeySpec(decodeKey,0,decodeKey.length,"AES");
               
                String packageNo;
                try {
                    packageNo = column[0];
                
                    String sender = symm.decrypt(column[1],decryptKey);
                    String receiver = symm.decrypt(column[4],decryptKey);
                    String deliveryType = symm.decrypt(column[8],decryptKey);
                    String escrowFee = symm.decrypt(column[9],decryptKey);
                    String status = column[14];
                            //symm.decrypt(column[14],decryptKey);
                    
                
                Object[] row = {packageNo, sender, receiver, deliveryType,escrowFee,status};
                tableModel.addRow(row);
                } catch (Exception ex) {
                    Logger.getLogger(ManagePackageGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            result.clear();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
    }
