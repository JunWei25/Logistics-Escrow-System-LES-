    private void initialisedDocumentListener() {
        DocumentListener inputListener = new DocumentListener() {
            private void updateButtonState() {
                boolean inputsFilled = Utils.textFieldsFilled(packageNoTextField,senderNameTextField,receipientNameTextField,deliveryTypeTextField,statusTextField);
                viewPackageButton.setEnabled(inputsFilled);
                updateDeliveryStatusButton.setEnabled(inputsFilled);
                if(statusTextField.getText().equals("Escrow Agreement Process")){
                    deliveryStatusComboBox.removeAllItems();
                    updateDeliveryStatusButton.setEnabled(false);
                }
                else if(statusTextField.getText().equals("Escrow Agreement Process Complete")){
                    deliveryStatusComboBox.removeAllItems();
                    deliveryStatusComboBox.addItem("In Sorting Facility");
                }
                else if(statusTextField.getText().equals("In Sorting Facility")){
                    deliveryStatusComboBox.removeAllItems();
                    deliveryStatusComboBox.addItem("Delivering");
                }
                else if(statusTextField.getText().equals("Delivering")){
                    deliveryStatusComboBox.removeAllItems();
                    deliveryStatusComboBox.addItem("Delivered");
                }
                else if(statusTextField.getText().equals("Delivered")){
                    deliveryStatusComboBox.removeAllItems();
                    deliveryStatusComboBox.addItem("Completed");
                }
                else{
                    deliveryStatusComboBox.removeAllItems();
                    updateDeliveryStatusButton.setEnabled(false);
                }
            }

            @Override
            public void insertUpdate(DocumentEvent e) {
                updateButtonState();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updateButtonState();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                updateButtonState();
            }

        };

        receipientNameTextField.getDocument().addDocumentListener(inputListener);
        packageNoTextField.getDocument().addDocumentListener(inputListener);
        senderNameTextField.getDocument().addDocumentListener(inputListener);
        deliveryTypeTextField.getDocument().addDocumentListener(inputListener);
        statusTextField.getDocument().addDocumentListener(inputListener);
    }

