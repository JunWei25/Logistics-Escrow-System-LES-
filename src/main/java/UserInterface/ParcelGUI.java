    private void decryptDataButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_decryptDataButtonMouseClicked
        // TODO add your handling code here:
        SymmetricEncryption symm = new SymmetricEncryption();
        String enteredEncryptedKey = JOptionPane.showInputDialog("Insert Key To Decrypt Data ").trim();
        String line = "";
        String[] column;
        String senderName="";
        String senderContact="";
        String senderAddress="";
        String receipientName="";
        String receipientContact="";
        String receipientAddress="";
        String parcelWeight="";
        String deliveryType="";
        String packagePayment="";
        String packagePaymentStatus="";
        String serviceFee="";
        String servicePaymentStatus="";
        String parcelStatus="";
        String parcelDeliveryStatus="";
        
        try {
            File myObj = new File("database/Parcel.txt");
            Scanner readFile = new Scanner(myObj);
            while (readFile.hasNextLine()) {
                line = readFile.nextLine().toString();
                if(line.contains(viewParcel.getPackageNo())){
                    result.add(line);                
                }
            }
            for(int i=0; i<result.size(); i++){
                column = result.get(i).split("!");
                String secretKey = column[15];
//                String hashedPassword = Hasher.sha256(user.getPassword());
                if(enteredEncryptedKey.equals(secretKey)){
                    byte[] decodeKey = Base64.getDecoder().decode(enteredEncryptedKey);
                    Key decryptKey = new SecretKeySpec(decodeKey,0,decodeKey.length,"AES");
                    senderName = symm.decrypt(senderNameTextField.getText(), decryptKey);
                    senderContact = symm.decrypt(senderContactTextField.getText(), decryptKey);
                    senderAddress = symm.decrypt(senderAddressTextField.getText(), decryptKey);
                    receipientName = symm.decrypt(receipientNameTextField.getText(), decryptKey);
                    receipientContact = symm.decrypt(receipientContactTextField.getText(), decryptKey);
                    receipientAddress = symm.decrypt(receipientAddressTextField.getText(), decryptKey);
                    deliveryType = symm.decrypt(deliveryTypeTextField.getText(), decryptKey);
                    parcelWeight = symm.decrypt(packageWeightTextField.getText(), decryptKey);
                    packagePayment = symm.decrypt(escrowFeeTextField.getText(), decryptKey);
                    serviceFee = symm.decrypt(serviceFeeTextField.getText(), decryptKey);
                    parcelStatus = symm.decrypt(statusTextField.getText(), decryptKey);
                    
                    parcelDeliveryStatus = deliveryStatusTextField.getText().trim();
                    if(parcelDeliveryStatus.length()>2){
//                        parcelDeliveryStatus = symm.decrypt(parcelDeliveryStatus, decryptKey);
                    }else{
                        parcelDeliveryStatus = parcelDeliveryStatus;
                    }
                    senderNameTextField.setText(senderName);
                    senderContactTextField.setText(senderContact);
                    senderAddressTextField.setText(senderAddress);
                    receipientNameTextField.setText(receipientName);
                    receipientContactTextField.setText(receipientContact);
                    receipientAddressTextField.setText(receipientAddress);
                    deliveryTypeTextField.setText(deliveryType);
                    packageWeightTextField.setText(parcelWeight);
                    escrowFeeTextField.setText(packagePayment);
                    serviceFeeTextField.setText(serviceFee);
                    statusTextField.setText(parcelStatus);
                    deliveryStatusTextField.setText(parcelDeliveryStatus);
                }
            }
            
            result.clear();
            readFile.close();
            
        }catch(Exception e){
           
        }
    }//GEN-LAST:event_decryptDataButtonMouseClicked

