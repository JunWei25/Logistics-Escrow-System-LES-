    private void loadParcelDetails(Parcel viewParcel){
        try {
            File myObj = new File("database/Parcel.txt");
            Scanner readFile = new Scanner(myObj);
            while (readFile.hasNextLine()) {
                String line = readFile.nextLine().toString();
                if(line.contains(viewParcel.getPackageNo())){
                    result.add(line);
                    break;
                };
            }
            String[] column = result.get(0).split("!");
            packageNoTextField.setText(column[0]);
            senderNameTextField.setText(column[1]);
            senderContactTextField.setText(column[2]);
            senderAddressTextField.setText(column[3]);
            receipientNameTextField.setText(column[4]);
            receipientContactTextField.setText(column[5]);
            receipientAddressTextField.setText(column[6]);
            deliveryTypeTextField.setText(column[8]);
            packageWeightTextField.setText(column[7]);
            escrowFeeTextField.setText(column[9]);
            serviceFeeTextField.setText(column[11]);
            statusTextField.setText(column[13]);
            deliveryStatusTextField.setText(column[14]);
         } catch (FileNotFoundException e) {
            e.printStackTrace();
        }

    }
